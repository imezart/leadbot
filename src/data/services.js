/**
 * Returns a category by its ID.
 * @param {string} catId
 */
export function getCategoryById(catId) {
  return categories.find((c) => c.id === catId) ?? null;
}

/**
 * Returns a service by category ID and service ID.
 * @param {string} catId
 * @param {string} svcId
 */
export function getServiceById(catId, svcId) {
  const cat = getCategoryById(catId);
  return cat?.services.find((s) => s.id === svcId) ?? null;
}
